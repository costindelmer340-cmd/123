DROP PROCEDURE IF EXISTS seed_twenty_mall_50_accounts;
DELIMITER //
CREATE PROCEDURE seed_twenty_mall_50_accounts()
BEGIN
  DECLARE i INT DEFAULT 0;
  DECLARE j INT DEFAULT 0;
  DECLARE account_id BIGINT;
  DECLARE account_no VARCHAR(64);
  DECLARE order_count INT;
  DECLARE order_id BIGINT;
  DECLARE item_id BIGINT;
  DECLARE product_id BIGINT;
  DECLARE product_price DECIMAL(12,2);
  DECLARE quantity INT;
  DECLARE total DECIMAL(12,2);
  DECLARE order_no VARCHAR(64);
  DECLARE order_status VARCHAR(32);
  DECLARE logistics_status VARCHAR(32);
  DECLARE after_sale_status VARCHAR(32);
  DECLARE item_after_sale VARCHAR(32);

  WHILE i < 50 DO
    SET account_id = 1001 + i;
    SET account_no = CONCAT('2023', LPAD(200 + i, 4, '0'));

    INSERT INTO twenty_mall_account (id, account_no, password_plain, account_role, display_name, phone, bind_status, status, deleted)
    VALUES (account_id, account_no, '123456', 'CONSUMER', CONCAT('20商城批量买家', i + 1), CONCAT('13620', LPAD(i, 6, '0')), 'UNBOUND', 'ACTIVE', 0)
    ON DUPLICATE KEY UPDATE password_plain = VALUES(password_plain), display_name = VALUES(display_name), phone = VALUES(phone), status = VALUES(status), deleted = 0;

    SET order_count = (i MOD 5) + 1;
    SET j = 0;
    WHILE j < order_count DO
      SET order_id = 1001 + i * 5 + j;
      SET item_id = 1001 + i * 5 + j;
      SET product_id = 1001 + ((i * 3 + j) MOD 30);
      SELECT price INTO product_price FROM twenty_mall_product WHERE id = product_id;
      SET quantity = 1 + ((i + j) MOD 2);
      SET total = product_price * quantity;
      SET order_no = CONCAT('TM20260628', LPAD(order_id - 1000, 4, '0'));
      SET order_status = CASE (i + j) MOD 4 WHEN 0 THEN 'COMPLETED' WHEN 1 THEN 'SHIPPED' WHEN 2 THEN 'PAID' ELSE 'COMPLETED' END;
      SET logistics_status = CASE order_status WHEN 'COMPLETED' THEN 'RECEIVED' WHEN 'SHIPPED' THEN 'IN_TRANSIT' ELSE 'WAIT_SHIPPING' END;
      SET after_sale_status = IF((i + j) MOD 4 = 0, 'AFTER_SALE', 'NONE');
      SET item_after_sale = IF(after_sale_status = 'AFTER_SALE', 'APPLIED', 'NONE');

      INSERT INTO twenty_mall_order (id, order_no, consumer_account_id, merchant_account_id, order_status, pay_status, logistics_status, after_sale_status, total_amount, paid_at, ordered_at, deleted)
      VALUES (order_id, order_no, account_id, 2, order_status, 'PAID', logistics_status, after_sale_status, total, '2026-06-28 10:10:00', '2026-06-28 10:00:00', 0)
      ON DUPLICATE KEY UPDATE consumer_account_id = VALUES(consumer_account_id), merchant_account_id = VALUES(merchant_account_id), order_status = VALUES(order_status), pay_status = VALUES(pay_status), logistics_status = VALUES(logistics_status), after_sale_status = VALUES(after_sale_status), total_amount = VALUES(total_amount), paid_at = VALUES(paid_at), ordered_at = VALUES(ordered_at), deleted = 0;

      INSERT INTO twenty_mall_order_item (id, order_id, product_id, product_name, sku_name, product_image_url, unit_price, quantity, total_amount, after_sale_status, deleted)
      SELECT item_id, order_id, p.id, p.product_name, CONCAT(p.category, '｜标准款｜默认规格'), p.product_image_url, p.price, quantity, total, item_after_sale, 0
      FROM twenty_mall_product p WHERE p.id = product_id
      ON DUPLICATE KEY UPDATE product_name = VALUES(product_name), sku_name = VALUES(sku_name), product_image_url = VALUES(product_image_url), unit_price = VALUES(unit_price), quantity = VALUES(quantity), total_amount = VALUES(total_amount), after_sale_status = VALUES(after_sale_status), deleted = 0;

      IF after_sale_status = 'AFTER_SALE' THEN
        INSERT INTO twenty_mall_after_sale (id, after_sale_no, order_id, order_item_id, after_sale_type, reason_type, description, requested_amount, status, deleted)
        VALUES (1001 + i * 5 + j, CONCAT('TMAS20260628', LPAD(order_id - 1000, 4, '0')), order_id, item_id, 'RETURN_REFUND', 'PRODUCT_QUALITY', '商品存在质量问题，申请售后处理。', total, 'PROCESSING', 0)
        ON DUPLICATE KEY UPDATE description = VALUES(description), requested_amount = VALUES(requested_amount), status = VALUES(status), deleted = 0;
      END IF;

      SET j = j + 1;
    END WHILE;
    SET i = i + 1;
  END WHILE;
END//
DELIMITER ;

CALL seed_twenty_mall_50_accounts();
DROP PROCEDURE IF EXISTS seed_twenty_mall_50_accounts;
